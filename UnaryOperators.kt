package com.example.first_kotlin_project

fun main(args: Array<String>) {

    var num:Double = 7.4
    var isCheck:Boolean = true

    println("+num = " + +num)
    println("-num = " + -num)
    println("++num = " + ++num)
    println("--num = " + --num)
    println("!isCheck = " + !isCheck)

    println("---------------------------------")

    var result:Double = 4.7

    println("result++ = " + result++ )
    println("result = " + result )
    println("++result = " + ++result )
    println("result = " + result )








}